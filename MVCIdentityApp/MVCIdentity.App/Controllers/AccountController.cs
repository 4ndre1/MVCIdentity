        // GET: /Account/Register
        [AllowAnonymous]
        public ActionResult Register()
        {
            return View();
        }

        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = new User { UserName = model.Email, Email = model.Email };
                var result = await UserManager.CreateAsync(user, model.Senha);
                if (result.Succeeded)
                {
                    //Não autenticar o usuário até confirmar o email!
                    //await SignInManager.SignInAsync(user, isPersistent:false, rememberBrowser:false);
                    
                    // For more information on how to enable account confirmation and password reset please visit https://go.microsoft.com/fwlink/?LinkID=320771
                    // Send an email with this link
                    string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                    var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                    await UserManager.SendEmailAsync(user.Id, "Confirme seu registro!", "Por favor confirma sua conta clicando neste link: \n\n" + callbackUrl);

                    ViewBag.Mensagem = "Verifique seu e-mail e confirme seu endereço.<br> Caso o e-mail não chegue clique neste <a href=\"" + 
                                       Url.Action("GenerateTokenEmailAgain", new { userId = user.Id }) + "\">link</a> para enviar o email novamente!";
                    
                    //return RedirectToAction("Index", "Home");

                    return View("Login");
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

        //
        // GET: /Account/GenerateTokenEmailAgain
        [AllowAnonymous]
        public async Task<ActionResult> GenerateTokenEmailAgain(int userId)
        {
            var user = await UserManager.FindByIdAsync(userId);
            if (user == null)
            {
                return View("Error");
            }

            //Caso o usuário já tenha confirmado seu registro, redirecionar para o login!
            if (user.EmailConfirmed)
            {
                return RedirectToAction("Login");
            }

            string code = await UserManager.GenerateEmailConfirmationTokenAsync(userId);

            var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = userId, code = code }, protocol: Request.Url.Scheme);
            await UserManager.SendEmailAsync(userId, "Confirme seu registro!", "Por favor confirma sua conta clicando neste link: \n\n" + callbackUrl);

            ViewBag.Mensagem = "Verifique seu e-mail e confirme seu endereço.<br> Caso o e-mail não chegue clique neste <a href=\"" + Url.Action("GenerateTokenEmailAgain", new { userId = userId }) + "\">link</a> para enviar o email novamente!";

            return View("Login");
        }

        //
        // GET: /Account/ConfirmEmail
        [AllowAnonymous]
        public async Task<ActionResult> ConfirmEmail(int userId, string code)
        {
            if (userId == 0 || code == null)
            {
                return View("Error");
            }

            var result = await UserManager.ConfirmEmailAsync(userId, code);

            if (result.Succeeded)
            {
                var user = await UserManager.FindByIdAsync(userId);

                await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);

                return View("ConfirmEmail");
            }

            return View("Error");
        }

        //
        // GET: /Account/ForgotPassword
        [AllowAnonymous]
        public ActionResult ForgotPassword()
        {
            return View();
        }

        //
        // POST: /Account/ForgotPassword
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> ForgotPassword(ForgotPasswordViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = await UserManager.FindByNameAsync(model.Email);
                if (user == null || !(await UserManager.IsEmailConfirmedAsync(user.Id)))
                {
                    // Don't reveal that the user does not exist or is not confirmed
                    return View("ForgotPasswordConfirmation");
                }

                // For more information on how to enable account confirmation and password reset please visit https://go.microsoft.com/fwlink/?LinkID=320771
                // Send an email with this link
                string code = await UserManager.GeneratePasswordResetTokenAsync(user.Id);
                var callbackUrl = Url.Action("ResetPassword", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);		
                await UserManager.SendEmailAsync(user.Id, "Nova senha!", "Por favor para criar uma nova senha clica neste link: \n\n " + callbackUrl);
                return RedirectToAction("ForgotPasswordConfirmation", "Account");
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

